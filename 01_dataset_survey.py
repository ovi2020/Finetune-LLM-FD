def check_dataset_files(data_dir: str = "./data") -> dict:
    """
    Check which dataset files are already present on disk.
    Returns a dict mapping alias → list of found files.
    """
    expected_files = {
        "ccf":       ["creditcard.csv"],
        "ieee_cis":  ["train_transaction.csv", "train_identity.csv"],
        "paysim":    ["PS_20174392719_1491204439457_log.csv"],
        "ibm_aml":   ["HI-Small_Trans.csv"],
        "elliptic":  ["elliptic_txs_features.csv", "elliptic_txs_classes.csv",
                      "elliptic_txs_edgelist.csv"],
        "banksim":   ["bs140513_032310.csv"],
    }

    status = {}
    for alias, files in expected_files.items():
        found = []
        missing = []
        for f in files:
            path = os.path.join(data_dir, f)
            if os.path.exists(path):
                size_mb = os.path.getsize(path) / (1024 ** 2)
                found.append(f"{f} ({size_mb:.1f} MB)")
            else:
                missing.append(f)
        status[alias] = {"found": found, "missing": missing}

    print("\nDataset File Status:")
    for alias, info in status.items():
        print(f"  {alias:12s}: {len(info['found'])} found, {len(info['missing'])} missing")
        if info["missing"]:
            print(f"              Missing: {info['missing']}")

    return status


def export_registry_json(output_path: str = "./data/dataset_registry.json"):
    """Export dataset registry to JSON for downstream scripts."""
    os.makedirs(os.path.dirname(output_path), exist_ok=True)
    registry = []
    for ds in DATASET_REGISTRY:
        registry.append({
            "alias": ds.alias,
            "name": ds.name,
            "label_column": ds.label_column,
            "source_url": ds.source_url,
            "fraud_ratio": ds.fraud_ratio,
        })
    with open(output_path, "w") as f:
        json.dump(registry, f, indent=2)
    print(f"\nDataset registry exported to: {output_path}")


